import UIKit

extension UIFont {
    
    static func openSansReg20() -> UIFont? {
        return UIFont.init(name: "OpenSans-Regular", size: 20)
    }
    
    static func openSansReg26() -> UIFont? {
        return UIFont.init(name: "OpenSans-Regular", size: 26)
    }
    
    static func laoSangamMN20() -> UIFont? {
        return UIFont.init(name: "Lao Sangam MN", size: 20)
    }
    
    static func laoSangamMN18() -> UIFont? {
        return UIFont.init(name: "Lao Sangam MN", size: 18)
    }
}

